uint32_t crc32b(uint8_t *message, size_t size) {
     int i, j;
     uint32_t byte, crc, mask;

     i = 0;
     crc = 0xFFFFFFFF;
     for(int k = 0; k<size;k++) {
        byte = message[k];            // Get next byte.
        crc = crc ^ byte;
        for (j = 7; j >= 0; j--) {    // Do eight times.
           mask = -(crc & 1);
           crc = (crc >> 1) ^ (0xEDB88320 & mask);
        }
        i = i + 1;
     }
     return ~crc;
  }

